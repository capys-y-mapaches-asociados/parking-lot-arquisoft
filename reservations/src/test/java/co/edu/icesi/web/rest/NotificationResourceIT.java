    /**
     * Create an entity for this test.
     *
     * This is a static method, as tests for other entities might also need it,
     * if they test an entity which requires the current entity.
     */
    public static Notification createEntity(EntityManager em) {
        Notification notification = new Notification()
            .reservationId(DEFAULT_RESERVATION_ID)
            .message(DEFAULT_MESSAGE)
            .sentAt(DEFAULT_SENT_AT)
            .recipientId(DEFAULT_RECIPIENT_ID);
        // Add required entity
        Reservation reservation;
        if (TestUtil.findAll(em, Reservation.class).isEmpty()) {
            reservation = ReservationResourceIT.createEntity(em);
            em.persist(reservation);
            em.flush();
        } else {
            reservation = TestUtil.findAll(em, Reservation.class).get(0);
        }
        notification.getReservationIds().add(reservation);
        return notification;
    }

